package br.edu.projeto.model;

public class Nacionalidades {

    private Integer id;
    private String desc;


	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getDesc() {
		return desc;
	}

	public void setDesc(String usuario) {
		this.desc = usuario;
	}

}